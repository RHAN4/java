public class AtribuirVariaveis {
    public static void main(String[] args) {
        // Declração de variáveis
        String nome = "Marta";
        int idade = 30;
        double peso = 65.2;

        // Declaração de constante
        final String CPF = "123.456.789-00";
        // Exibindo resultados
        // Concatenando com o sinal "+"
        System.out.println("O nome do usuário: " + nome);
        System.out.println("O CPF do usuário: " + CPF);
        System.out.println("A idade do usuário: " + idade);
        System.out.println("O peso do usuário: " + peso);
    }
}
